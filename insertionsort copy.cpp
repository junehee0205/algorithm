void * insertionsort(int arr[], int n){
    for(int j=1; j<n; j++){
        int key = arr[j];
        int i = j-1;

        while(arr[i]>key && i > -1 ){
            arr[i+1] = arr[i];
            i--;
        }

        arr[i+1] = key;
    }
}


